import java.util.*;

public class Solution {
    public static void solve() {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        // check whether n is big or a number
        if(n>100){
            System.out.println("Big");
        }
        if(n>100){
            System.out.println("Number");
        }
        else{
            System.out.println("Number");
        }
    }
}